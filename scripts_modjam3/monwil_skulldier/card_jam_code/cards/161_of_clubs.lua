---@param player EntityPlayer
local function UseCard(_, _, player)
    local rng = player:GetCardRNG(mod.Card.THE_161_OF_CLUBS)
    for i = 1, ONE_HUNDRED_AND_SIXTY_ONE do
        local pos = Isaac.GetRandomPosition()
        local roll = picker:PickOutcome(rng)
        local variant, subType = table.unpack(OUTCOMES[roll])
        local pickup = Isaac.Spawn(EntityType.ENTITY_PICKUP, variant, subType, pos, RandomVector(), player):ToPickup()
        ---@cast pickup EntityPickup
        if variant == PickupVariant.PICKUP_BOMB and subType ~= BombSubType.BOMB_TROLL and subType ~= BombSubType.BOMB_GOLDENTROLL then
            pickup.Timeout = rng:RandomInt(MIN_PICKUP_TIMEOUT, MAX_PICKUP_TIMEOUT)
        end
    end
end
mod:AddCallback(ModCallbacks.MC_USE_CARD, UseCard, mod.Card.THE_161_OF_CLUBS)